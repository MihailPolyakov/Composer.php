
1. Пространство имен - это рспредление классов по разным дирректориям. Один класс равен одному файлу. Классы у разных разработчиков могут совпадать и чтоб этого не происходило, создается пространство имен для выбора определнного класса в определенной дирректории.

2. Исключения - это теже самые классы, которые имеет блоки для выбрасыания и ловли каких либо ошибок.

